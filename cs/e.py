#控制台计算器程序
# 作者：王宇
# 创建时间：2019/11/20 20:00
# 最后修改时间：2019/11/20 20:00
# 本程序是一个简单的控制台计算器程序
# 本程序的功能是实现一个简单的控制台计算器
# 本程序的输入是一个字符串，代表一个表达式
# 本程序的输出是一个字符串，代表表达式的计算结果
# 本程序支持的运算符有+ - * / ( )
# 本程序支持的操作数是非负整数
# 本程序不支持错误处理
# 本程序不支持表达式的合法性检查
# 本程序不支持表达式的格式检查
# 本程序不支持除数为0的情况
# 本程序的运行时间为O(n)
# 本程序的空间复杂度为O(n)
# 本程序的算法如下：
# 1. 从输入字符串中提取操作数和运算符
# 2. 根据运算符的优先级计算表达式的值
# 3. 输出表达式的值
# 4. 结束
